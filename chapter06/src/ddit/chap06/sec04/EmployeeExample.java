package ddit.chap06.sec04;

import java.util.Scanner;

public class EmployeeExample {
	Scanner sc = new Scanner(System.in);
	Employees[] employee = { new Employees("202310001", "정몽주", "공공개발팀"), 
							 new Employees("202111012", "김민우", "개발지원팀"),
							 new Employees("202011031", "이순신", "개발지원팀"), 
							 new Employees("202210001", "박자바", "공공개발팀"),
							 new Employees("202011001", "강자바", "공공개발팀") }; // 객체 배열

	public static void main(String[] args) {
		EmployeeExample emp = new EmployeeExample(); //시점을 현재로

		while (true) {
			int choice = emp.view(); // 선택창

			switch (choice) {
			case 1:
				emp.selectList();
				break;
			case 2:
				emp.selectOne();
				break;
			case 3:
				emp.changeSalary();
				break;
			case 9:
				System.out.println("프로그램을 종료합니다...");
				System.exit(0);
			default:
				System.out.println("작업번호를 잘못 선택했습니다...");
			}
		}
	}

	public int view() {
		System.out.println("[[사원조회]]");
		System.out.println(" 1. 전체 조회");
		System.out.println(" 2. 부서별 조회");
		System.out.println(" 3. 급여 설정");
		System.out.println(" 9. 종료");
		System.out.println("------------------------");
		System.out.print("선택 >> ");
		int choice = sc.nextInt();

		return choice;
	}


	public void selectList() {
		System.out.println("부서명            사원번호             이름   급여");
		System.out.println("---------------------------------");
		for (int i = 0; i < employee.length; i++) {
			System.out.printf("%-11s", employee[i].getDeptName());
			System.out.printf("%-11s", employee[i].getEmpID());
			System.out.printf("%-5s", employee[i].getEmpName());
			System.out.printf("%-20d\n", employee[i].getSalary());
		}
		System.out.println("---------------------------------");
	}

	public void selectOne() {
		System.out.print("조회할 부서명 : ");
		// String dept = sc.nextLine(); nextInt() 다음에 nextLine()에서 개행문자를 받은 상황
		String dept = sc.next();

		System.out.println("부서명            사원번호             이름   급여");
		System.out.println("---------------------------------");
		for (int i = 0; i < employee.length; i++) {
			if (dept.equals(employee[i].getDeptName())) {
				System.out.printf("%-11s", employee[i].getDeptName());
				System.out.printf("%-11s", employee[i].getEmpID());
				System.out.printf("%-5s", employee[i].getEmpName());
				System.out.printf("%-20d\n", employee[i].getSalary());
			}
		}
		System.out.println("---------------------------------");
	}

	public void changeSalary() {
		System.out.print("급여를 바꿀 사원의 번호 : ");
		String empID = sc.next();
		
		for (int i = 0; i < employee.length; i++) {
			if (empID.equals(employee[i].getEmpID())) {
				System.out.print("설정할 급여 : ");
				int salary = sc.nextInt();
				employee[i].setSalary(salary);
			}
			
		}
	}

}